package com.salamov.testproject.models;


public class ItemsAtHorizontalAdapter {

    private int image;
    private String name;

    public ItemsAtHorizontalAdapter(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }
}
